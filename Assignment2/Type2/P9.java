class P9{
	public static void main(String args[]){
for(int k=1;k<=5;k++)
{
	for(int l=k;l<5;l++){
		
			
				System.out.print(" ");
			
			}
		
	
	for(int m=1;m<=k;m++){
		
			
				System.out.print("*");
			
			}
		
		
		System.out.println("");
}
for(int a=1;a<=5;a++)
{
		for(int b=0;b<a;b++){
		
			
				System.out.print(" ");
			
			}
	for(int c=a;c<=4;c++){
		
			
				System.out.print("*");
			
			}
			System.out.println("");
	

		
		
		
}
}
}