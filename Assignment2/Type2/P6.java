class P6{
	public static void main(String args[]){
for(int k=1;k<=5;k++)
{
	for(int i=1;i<k;i++)
	{
		System.out.print(" ");
	}
	for(int l=5;l>k;l--)
	{
		System.out.print("*");
	}
	for(int j=5;j>=k;j--)
	{
		System.out.print("*");
	
	}
	for(int i=1;i<k;i++)
	{
		System.out.print(" ");
	}
	System.out.println();
}
}
}