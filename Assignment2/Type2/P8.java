class P8{
	public static void main(String args[]){
for(int k=1;k<=5;k++)
{
	for(int l=1;l<=k;l++){
		
			
				System.out.print("*");
			
			}
		System.out.println("");
}
for(int a=1;a<=5;a++)
{
		for(int b=5;b>a;b--){
		
			
				System.out.print("*");
			
			}
			System.out.println("");		
		
}
}
}