class P12{
	public static void main(String args[]){
for(int k=0;k<4;k++)
{
		for(int m=5;m>k;m--){
		
			
				System.out.print("*");
			
			}
	for(int l=1;l<=k;l++){
		
			
				System.out.print(" ");
			
			}
			System.out.println("");
}			
		
		for(int p=1;p<=5;p++)
		{
		for(int n=1;n<=p;n++){
		
			
				System.out.print("*");
			
			}
					
	for(int o=5;o>=p;o--){
		
			
				System.out.print(" ");
			
			}
			System.out.println("");
		}
}
}