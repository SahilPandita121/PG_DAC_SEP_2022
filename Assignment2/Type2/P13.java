class P13{
	public static void main(String args[]){
for(int k=1;k<=4;k++)
{
		for(int m=1;m<k;m++){
		
			
				System.out.print(" ");
			
			}
	for(int l=k;l<=5;l++){
		
			
				System.out.print("*");
			
			}
			System.out.println("");
}			
		
		for(int p=1;p<=5;p++)
		{
		for(int n=5;n>p;n--){
		
			
				System.out.print(" ");
			
			}
					
	for(int o=1;o<=p;o++){
		
			
				System.out.print("*");
			
			}
			System.out.println("");
		}
}
}